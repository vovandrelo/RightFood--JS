// 1. Модуль Gulp - предоставляет функционал для работы с  Gulp
import gulp from "gulp";

// 2. Модуль Newer - предоставляет информацию об обновлении картинок:
import newer from "gulp-newer";

// 3. Пользовательский модуль Path - предоставляет информацию о путях к необходимым файлам/папкам
import { path } from "../config/path.js";

/* <================================================================================================================> */
/* <================================== РЕАЛИЗАЦИЯ ЗАДАЧИ ПЕРЕМЕЩЕНИЯ ИЗОБРАЖЕНИЙ ===================================> */
/* <================================================================================================================> */

// Функция копирует все изображения из папки с исходниками в папку с результатом:
export const copyImages = () => {
    return gulp.src(path.src.images)            // Считываем из папки с исходниками необходимые изображения    
        .pipe(newer(path.dist.images))         // Вычисляем изображения, которые были изменены
        .pipe(gulp.dest(path.dist.images));    // Записываем необходимые изображения в папку с результатом
};